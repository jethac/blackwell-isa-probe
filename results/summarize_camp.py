#!/usr/bin/env python3
"""Regenerate the Blackwell datacenter-vs-consumer camp-split tables from the raw
data - nothing hand-typed. Mirrors summarize.py's "the JSON is the source of
truth" contract, for the camp analysis.

Inputs (all shipped in results/):
  results_full_ptx93.json   - the 993-probe x 6-target compile matrix (ptxas 13.3)
  results_ptx93.json        - the 23-probe PTX 9.3 delta compile matrix
  sass_camp_report2.json    - the rigorous SASS marker report (sink-wrapped)

Emits CAMP_SPLIT.md (and prints it). Run:  python results/summarize_camp.py
"""
import json, os
HERE = os.path.dirname(os.path.abspath(__file__))
def L(f): return json.load(open(os.path.join(HERE, f)))

DC   = ["sm_100a", "sm_103a", "sm_110a"]   # tcgen05 camp
CONS = ["sm_120a", "sm_120f", "sm_121a"]   # warp-block-scale camp
MECH = {"BPT","CALL","CALL.ABS","CALL.REL","ERRBAR","CGAERRBAR"}

def arch_reject(cell): return cell.get("status") == "REJECT" and cell.get("class") == "ARCH"
def accept(cell):      return cell.get("status") == "ACCEPT"

# ---- Tier 1: compile-level exclusives (one camp arch-rejects) ----
compile_mat = {**{k:v for k,v in L("results_full_ptx93.json").items()},
               **{k:v for k,v in L("results_ptx93.json").items()}}
consumer_only, datacenter_only, datacenter_subset = [], [], []
for k, v in compile_mat.items():
    res = v["res"]
    if not all(a in res for a in DC+CONS): continue
    cons_all = all(accept(res[a]) for a in CONS)
    cons_none = all(arch_reject(res[a]) for a in CONS)
    dc_all = all(accept(res[a]) for a in DC)
    dc_some = any(accept(res[a]) for a in DC)
    row = (v["family"], v["name"])
    if cons_all and all(arch_reject(res[a]) for a in DC):
        consumer_only.append(row)
    elif dc_all and cons_none:
        datacenter_only.append(row)
    elif cons_none and dc_some and not dc_all:   # B200/B300 but not Thor, etc.
        datacenter_subset.append(row + tuple([tuple(a for a in DC if accept(res[a]))]))

def by_family(rows):
    from collections import defaultdict
    g = defaultdict(list)
    for r in rows: g[r[0]].append(r[1])
    return g

# ---- Tiers 2 & 3: SASS splits (compile on all three, machine code differs) ----
rep = L("sass_camp_report2.json")
mech, enc = [], []
for k, v in rep["splits"].items():
    if v.get("cat") != "camp-split": continue
    diff = set(v["dc_only"]) | set(v["consumer_only"])
    row = (k, v["dc_only"], v["consumer_only"])
    (mech if diff & MECH else enc).append(row)

def famsum(rows):
    from collections import defaultdict
    g = defaultdict(list)
    for k, do, co in rows:
        g[(tuple(sorted(do)), tuple(sorted(co)))].append(k)
    return g

out = []
w = out.append
w("# Blackwell camp split - datacenter (tcgen05) vs consumer (warp-block-scale)\n")
w("*Machine-generated by `results/summarize_camp.py` from the shipped verdict "
  "matrices (`results_full_ptx93.json`, `results_ptx93.json`) + the rigorous SASS "
  "marker report (`sass_camp_report2.json`). Nothing hand-typed. Camps: datacenter "
  "= sm_100a / sm_103a / sm_110a; consumer = sm_120a / sm_120f / sm_121a. "
  "ptxas V13.3.73.*\n")
w(f"**Summary.** The Blackwell family splits by *tensor-core lineage*, not form "
  f"factor. {len(consumer_only)} instructions are consumer-only and "
  f"{len(datacenter_only)} datacenter-only at the compile level (+{len(datacenter_subset)} "
  f"datacenter-subset); of those that compile on both camps, {len(mech)} lower to a "
  f"different *mechanism* (consumer traps / software-emulates / degrades) and "
  f"{len(enc)} to a different *native encoding*. Every other instruction lowers "
  f"identically, and **sm120 == sm121** byte-for-byte.\n")

w("## Tier 1 - compile-level exclusives (one camp arch-rejects the other's)\n")
w(f"### Consumer-only ({len(consumer_only)}) - reject on all datacenter targets, by family")
for fam, ns in sorted(by_family(consumer_only).items()):
    w(f"- **{fam}** ({len(ns)}): " + ", ".join(f"`{n}`" for n in sorted(ns)))
w(f"\n### Datacenter-only ({len(datacenter_only)}) - reject on all consumer targets")
w("  " + ", ".join(f"`{n}`" for _, n in sorted(datacenter_only)))
if datacenter_subset:
    w(f"\n### Datacenter-subset ({len(datacenter_subset)}) - reject on all consumer, accept on only SOME datacenter")
    for fam, name, accs in sorted(datacenter_subset):
        w(f"- `{name}` (accepts on {', '.join(accs)})")

w("\n## Tier 2 - mechanism splits (compile on both; consumer traps / emulates / degrades)\n")
w("| datacenter markers | consumer markers | n |")
w("|---|---|---|")
for (do, co), ks in famsum(mech).items():
    w(f"| {list(do) or '-'} | {list(co) or '-'} | {len(ks)} |")
    for k in ks: w(f"| | `{k.split('|')[1]}` | |")

w("\n## Tier 3 - native-encoding differences (both native, different instruction)\n")
w("| datacenter | consumer | n |")
w("|---|---|---|")
for (do, co), ks in famsum(enc).items():
    w(f"| {list(do)} | {list(co)} | {len(ks)} |")
    for k in ks: w(f"| | `{k.split('|')[1]}` | |")

text = "\n".join(out) + "\n"
open(os.path.join(HERE, "CAMP_SPLIT.md"), "w", encoding="utf-8").write(text)
print(text)
